let x = 5;
let y = 2;
let z = 1; 

z = Math.round(x); // rounded of to the nearest integer
z = Math.floor(x); //rounded of to the greatest integer
z = Math.ceil(x); //rounded of to the smallest integer
z = Math.trunc(x); //removes all the decimal digits
z = Math.pow(x, y); //returns the power
z = Math.sqrt(x); //returns the square root of an integer
z = Math.log(x); //returns the log of the function
let max = Math.max(x, y, z); // finds the maximum value

let min = Math.min(x, y, z); //finds the minimum value


